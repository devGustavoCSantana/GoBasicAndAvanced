package main

import "fmt"

// Função principal que é executada quando o programa é iniciado.
func main() {
	// A função fmt.Printf permite formatar a string de saída.
	// %T exibe o tipo da variável.
	// %v exibe o valor da variável.

	// A primeira linha imprime o tipo e o valor da string "1".
	fmt.Printf("Type: %T - Value: %v\n", "1", "1")

	// A segunda linha imprime o tipo e o valor da string "1".
	fmt.Printf("Type: %T - Value: %v\n", "1", "1")

	// A terceira linha imprime o tipo e o valor da string "1".
	fmt.Printf("Type: %T - Value: %v\n", "1", "1")

	// A quarta linha imprime o tipo e o valor da string "1".
	fmt.Printf("Type: %T - Value: %v\n", "1", "1")
}
